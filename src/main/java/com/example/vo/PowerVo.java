package com.example.vo;

import java.util.List;

public class PowerVo {
    private String telephone;
    private String userName;
    private String company;
    private String position;
    private String companyPower;
    private String businessPower;
    private String dutyPower;
    private List<String> companyValue;
    private List<String> businessValue;
    private List<String> dutyValue;
    
	public String getTelephone() {
		return telephone;
	}
	public void setTelephone(String telephone) {
		this.telephone = telephone;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public String getCompanyPower() {
		return companyPower;
	}
	public void setCompanyPower(String companyPower) {
		this.companyPower = companyPower;
	}
	public String getBusinessPower() {
		return businessPower;
	}
	public void setBusinessPower(String businessPower) {
		this.businessPower = businessPower;
	}
	public String getDutyPower() {
		return dutyPower;
	}
	public void setDutyPower(String dutyPower) {
		this.dutyPower = dutyPower;
	}
	public List<String> getCompanyValue() {
		return companyValue;
	}
	public void setCompanyValue(List<String> companyValue) {
		this.companyValue = companyValue;
	}
	public List<String> getBusinessValue() {
		return businessValue;
	}
	public void setBusinessValue(List<String> businessValue) {
		this.businessValue = businessValue;
	}
	public List<String> getDutyValue() {
		return dutyValue;
	}
	public void setDutyValue(List<String> dutyValue) {
		this.dutyValue = dutyValue;
	}
	
}
